string = "Hello World!"
print(string[3:7]) #part of string
print(string[3:10:2]) #part of string
print(string[::-1]) #reverse
print(string.upper()) #upper
print(string.lower()) #lower
print(string.startswith("Hello")) #check the start of sentence(True or False)
print(string.endswith("python")) #check the end of sentence(True or False)
print(string.split(" ")) #split sentence with SPACE
